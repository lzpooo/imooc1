package com.briup.day02.buildexercise;

public class Person {
	private Head head;
	private Body body;
	private Legs legs;

	public Head getHead() {
		return head;
	}

	public void setHead(Head head) {
		this.head = head;
	}

	public Body getBody() {
		return body;
	}

	public void setBody(Body body) {
		this.body = body;
	}

	public Legs getLegs() {
		return legs;
	}

	public void setLegs(Legs legs) {
		this.legs = legs;
	}
}
